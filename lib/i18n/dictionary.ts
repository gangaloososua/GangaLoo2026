const messages: Record<Locale, Messages> = { en, es }

export function t(locale: Locale, key: string): string {
  return messages[locale][key] ?? messages.en[key] ?? key
}

export function plural(
  locale: Locale,
  n: number,
  oneKey: string,
  otherKey: string,
): string {
  return t(locale, n === 1 ? oneKey : otherKey)
}
